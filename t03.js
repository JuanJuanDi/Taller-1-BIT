//Calcular el área de un rectángulo utilizando variables para la base y la altura, y luego imprimir el resultado utilizando console.log.

// Declarar variables para la base y la altura del rectángulo
const base = 4;
const altura = 8;

// Calcular el área del rectángulo (base * altura)
const area = base * altura;

// Imprimir el resultado utilizando console.log
console.log("El área del rectángulo es: " + area);